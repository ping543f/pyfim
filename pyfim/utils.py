def _type_of_script():
    """ Returns context in which pyFIM is run. """
    try:
        ipy_str = str(type(get_ipython()))
        if 'zmqshell' in ipy_str:
            return 'jupyter'
        if 'terminal' in ipy_str:
            return 'ipython'
    except:
        return 'terminal'

def is_jupyter():
    """ Test if pyFIM is run in a Jupyter notebook."""
    return _type_of_script() == 'jupyter'

